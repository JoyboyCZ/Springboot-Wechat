<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
	import xiaEditor from '@/components/xia-editor/xia-editor';
	import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				jihuabiaoti: '',
				yundongleixing: '',
				tupian: '',
				xunlianfangshi: '',
				xunlianpinlv: '',
				yuqimubiao: '',
				shijimubiao: '',
				yundongzhuangtai: '',
				beizhu: '',
				jilushijian: '',
				yonghuzhanghao: '',
				yonghuxingming: '',
				crossuserid: '',
				crossrefid: '',
				},
				yundongzhuangtaiOptions: [],
				yundongzhuangtaiIndex: 0,
				// 登录用户信息
				user: {},
				ro:{
				   jihuabiaoti : false,
				   yundongleixing : false,
				   tupian : false,
				   xunlianfangshi : false,
				   xunlianpinlv : false,
				   yuqimubiao : false,
				   shijimubiao : false,
				   yundongzhuangtai : false,
				   beizhu : false,
				   jilushijian : false,
				   yonghuzhanghao : false,
				   yonghuxingming : false,
				   crossuserid : false,
				   crossrefid : false,
				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			this.ruleForm.jilushijian = this.$utils.getCurDate();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.yonghuzhanghao = this.user.yonghuzhanghao
			this.ro.yonghuzhanghao = true;
			this.ruleForm.yonghuxingming = this.user.yonghuxingming
			this.ro.yonghuxingming = true;


			// 自定义下拉框值
			this.yundongzhuangtaiOptions = "已完成,未完成".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jianshenjilu`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='jihuabiaoti'){
						this.ruleForm.jihuabiaoti = obj[o];
						this.ro.jihuabiaoti = true;
						continue;
					}
					if(o=='yundongleixing'){
						this.ruleForm.yundongleixing = obj[o];
						this.ro.yundongleixing = true;
						continue;
					}
					if(o=='tupian'){
						this.ruleForm.tupian = obj[o].split(",")[0];
						this.ro.tupian = true;
						continue;
					}
					if(o=='xunlianfangshi'){
						this.ruleForm.xunlianfangshi = obj[o];
						this.ro.xunlianfangshi = true;
						continue;
					}
					if(o=='xunlianpinlv'){
						this.ruleForm.xunlianpinlv = obj[o];
						this.ro.xunlianpinlv = true;
						continue;
					}
					if(o=='yuqimubiao'){
						this.ruleForm.yuqimubiao = obj[o];
						this.ro.yuqimubiao = true;
						continue;
					}
					if(o=='shijimubiao'){
						this.ruleForm.shijimubiao = obj[o];
						this.ro.shijimubiao = true;
						continue;
					}
					if(o=='yundongzhuangtai'){
						this.ruleForm.yundongzhuangtai = obj[o];
						this.ro.yundongzhuangtai = true;
						continue;
					}
					if(o=='beizhu'){
						this.ruleForm.beizhu = obj[o];
						this.ro.beizhu = true;
						continue;
					}
					if(o=='jilushijian'){
						this.ruleForm.jilushijian = obj[o];
						this.ro.jilushijian = true;
						continue;
					}
					if(o=='yonghuzhanghao'){
						this.ruleForm.yonghuzhanghao = obj[o];
						this.ro.yonghuzhanghao = true;
						continue;
					}
					if(o=='yonghuxingming'){
						this.ruleForm.yonghuxingming = obj[o];
						this.ro.yonghuxingming = true;
						continue;
					}
					if(o=='crossuserid'){
						this.ruleForm.crossuserid = obj[o];
						this.ro.crossuserid = true;
						continue;
					}
					if(o=='crossrefid'){
						this.ruleForm.crossrefid = obj[o];
						this.ro.crossrefid = true;
						continue;
					}
				}
			}
			this.styleChange()
			this.$forceUpdate()
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap()
				}, 300)
			}
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数

			jilushijianChange(e) {
				this.ruleForm.jilushijian = e.target.value;
				this.$forceUpdate();
			},


			// 下拉变化
			yundongzhuangtaiChange(e) {
				this.yundongzhuangtaiIndex = e.target.value
				this.ruleForm.yundongzhuangtai = this.yundongzhuangtaiOptions[this.yundongzhuangtaiIndex]
			},

			tupianTap() {
				let _this = this;
				if(this.ro.tupian){
					return false
				}
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				let that = this
				//跨表计算判断
				var obj;
				if((!this.ruleForm.jihuabiaoti)){
					this.$utils.msg(`计划标题不能为空`);
					return
				}
				//更新跨表属性
				var crossuserid;
				var crossrefid;
				var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
						if(!obj) {
							obj = uni.getStorageSync('crossObj');
						}
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
							   crossuserid=Number(uni.getStorageSync('appUserid'));
							   crossrefid=obj['id'];
							   crossoptnum=uni.getStorageSync('statusColumnName');
							   crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`jianshenjilu`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						uni.removeStorageSync('crossCleanType');
						return false;
					} else {
				//跨表计算
						let oet = {}
						if(this.ruleForm.id){
							await this.$api.update(`jianshenjilu`, this.ruleForm);
						}else{
							oet = await this.$api.add(`jianshenjilu`, this.ruleForm);
						}
						if(this.cross){
							uni.setStorageSync('crossCleanType',true);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
				//跨表计算
					let oet = {}
					if(this.ruleForm.id){
						await this.$api.update(`jianshenjilu`, this.ruleForm);
					}else{
						oet = await this.$api.add(`jianshenjilu`, this.ruleForm);
					}
					if(this.cross){
						uni.setStorageSync('crossCleanType',true);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				if(this.ro[str]){
					return false
				}
				this.$refs[str].show();
			},
		}
	}
</script>

