					],
					zhongcansheru: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					wancansheru: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					jiacansheru: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					sheruzongreliang: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					dengjishijian: [
					],
					beizhu: [
					],
					thumbsupnum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					crazilynum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					userid: [
					],
				},
			};
		},
		props: ["parent"],
		computed: {
			sheruzongreliang: {
				get: function () {
					return 0+parseFloat(this.ruleForm.zaocansheru==""?0:this.ruleForm.zaocansheru)+parseFloat(this.ruleForm.zhongcansheru==""?0:this.ruleForm.zhongcansheru)+parseFloat(this.ruleForm.wancansheru==""?0:this.ruleForm.wancansheru)+parseFloat(this.ruleForm.jiacansheru==""?0:this.ruleForm.jiacansheru) || 0
				}
			},



		},
