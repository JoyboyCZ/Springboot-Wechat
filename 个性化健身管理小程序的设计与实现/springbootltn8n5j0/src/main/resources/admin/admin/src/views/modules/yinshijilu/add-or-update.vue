					yonghuxingming : false,
					biaoti : false,
					riqi : false,
					tupian : false,
					zaocansheru : false,
					zhongcansheru : false,
					wancansheru : false,
					jiacansheru : false,
					sheruzongreliang : false,
					dengjishijian : false,
					beizhu : false,
					thumbsupnum : false,
					crazilynum : false,
					userid : false,
				},
			
				ruleForm: {
					yonghuzhanghao: '',
					yonghuxingming: '',
					biaoti: '',
					riqi: '',
					tupian: '',
					zaocansheru: Number('0'),
					zhongcansheru: Number('0'),
					wancansheru: Number('0'),
					jiacansheru: Number('0'),
					sheruzongreliang: '',
					dengjishijian: '',
					beizhu: '',
					userid: '',
				},

				rules: {
					yonghuzhanghao: [
